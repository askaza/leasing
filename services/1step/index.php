<h3>Контроль качества выполнения проекта </h3>
 
<p>Все этапы проекта документируются, это позволяет избежать разночтений и быстрее добиться поставленной цели. В состав проектной документации входят: Устав проекта, План проекта, ФТТ, Технический проект, Протоколы встречи и д.р. </p>
 
<p>Функциональные технические требования по доработке системы &mdash; один из наиболее важных и сложных для прочтения документов. Разработка ФТТ в формате сценариев использования системы (методология А. Коберна), позволяет сделать его максимально понятным пользователям и существенно снижает риски и сроки проекта. </p>
 
