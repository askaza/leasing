			<ul>
				<li>Компания «Хомнет Лизинг» заслужила репутацию самого надежного партнера лизинговых компаний в области комплексной автоматизации и выпускает решения, аналогов которым в России пока нет.</li>

				<li>Однако «Хомнет Лизинг» не планирует останавливаться на достигнутом и продолжает делать всё, чтобы решения «Хомнет Лизинг» были самым эффективным инструментом развития лизинговых компаний России и СНГ.</li>
