			<p><strong>2000</strong></p>

			<ul>
				<li>Основание компании &laquo;1С:Хомнет&raquo;. </li>

				<li>Определение стратегии компании &mdash; сотрудничество с корпоративным сектором и построение долгосрочных отношений с клиентами. </li>
